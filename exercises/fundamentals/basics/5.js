let num1 = parseInt(prompt('Enter the first number:'), 10);
console.log(num1);
let num2 = parseInt(prompt('Enter the second number:'), 10);
console.log(num2);

console.log(`${num1} + ${num2} = ${num1 + num2}`);
console.log(`${num1} - ${num2} = ${num1 - num2}`)
console.log(`${num1} * ${num2} = ${num1 * num2}`)
console.log(`${num1} / ${num2} = ${parseInt(num1 / num2, 10)}`)
console.log(`${num1} % ${num2} = ${num1 % num2}`)
console.log(`${num1} ** ${num2} = ${num1 ** num2}`)
